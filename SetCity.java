    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String city = jTextField1.getText();

        if (city.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the city name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet rs = MySQL.search("SELECT * FROM `city` WHERE `name` = '" + city + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This city already exists, Please select it from the following table", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.iud("INSERT INTO `city`(`name`) VALUES('" + city + "')");
                    LoadCities();
                    jTextField1.setText("");
                    JOptionPane.showMessageDialog(this, "A new city added", "Success", JOptionPane.INFORMATION_MESSAGE);

                }
            } catch (Exception e) {
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked

        if (evt.getClickCount() == 2) {

            int r = jTable1.getSelectedRow();
            String c_id = jTable1.getValueAt(r, 0).toString();

            this.sa.jLabel4.setText(c_id);
            this.dispose();

        }

    }//GEN-LAST:event_jTable1MouseClicked

