    public void LoadAddress() {

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `address` INNER JOIN `city` ON `address`.`city_id` = `city`.`id`");

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("address.id"));
                v.add(rs.getString("address.line1"));
                v.add(rs.getString("address.line2"));
                v.add(rs.getString("city.name"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

