    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String line1 = jTextField2.getText();
        String line2 = jTextField3.getText();
        String c_id = jLabel4.getText();

        if (line1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Line 1", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (line2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter Line 1", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (c_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select city", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs = MySQL.search("SELECT * FROM `address` WHERE `line1` = '" + line1 + "' AND `line2` = '" + line2 + "' AND `city_id` = '" + c_id + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This address already has exists, Select it from the following table", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    MySQL.iud("INSERT INTO `address`(`line1`,`line2`,`city_id`) VALUES('" + line1 + "','" + line2 + "','" + c_id + "')");
                    LoadAddress();

                    jTextField2.setText("");
                    jTextField3.setText("");
                    jLabel4.setText("None");

                    JOptionPane.showMessageDialog(this, "A new adress added", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {

            int r = jTable1.getSelectedRow();
            String a_id = jTable1.getValueAt(r, 0).toString();
            
            if(Objects.isNull(this.tr)){
                this.sr.jLabel9.setText(a_id);
            }else if(Objects.isNull(this.sr)){
                String line1 = jTable1.getValueAt(r, 1).toString();
                String line2 = jTable1.getValueAt(r, 2).toString();
                String city = jTable1.getValueAt(r, 3).toString();
                this.tr.jLabel6.setText(a_id);
                this.tr.jLabel7.setText(line1+","+line2+","+city);
                             
            }
            
            
            this.dispose();

        }
    }//GEN-LAST:event_jTable1MouseClicked

