    public void LoadTeachers() {

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `teacher` INNER JOIN `gender` ON `teacher`.`gender_id` = `gender`.`id` "
                    + "INNER JOIN `status` ON `status`.`id` = `teacher`.`status_id` "
                    + "INNER JOIN `address` ON `address`.`id` = `teacher`.`address_id` "
                    + "INNER JOIN `city` ON `city`.`id` = `address`.`city_id` ORDER BY `teacher`.`id` ASC");

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("teacher.id"));
                v.add(rs.getString("teacher.name"));
                v.add(rs.getString("teacher.mobile"));
                v.add(rs.getString("teacher.email"));
                v.add(rs.getString("gender.name"));
                v.add(rs.getString("address.line1") + "," + rs.getString("address.line2") + rs.getString("city.name"));
                v.add(rs.getString("status.name"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
        }

    }

