    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String name = jTextField1.getText();
        String mobile = jTextField2.getText();
        String email = jTextField3.getText();
        String gender = jComboBox1.getSelectedItem().toString();
        String a_id = jLabel6.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[01245678][0-9]{7}").matcher(mobile).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter email", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (gender.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select gender", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (a_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Address", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet rs1 = MySQL.search("SELECT * FROM `gender` WHERE `name` = '" + gender + "'");
                rs1.next();
                String g_id = rs1.getString("id");

                ResultSet rs = MySQL.search("SELECT * FROM `teacher` WHERE `mobile` = '" + mobile + "' OR `email` = '" + email + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This teacher already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.iud("INSERT INTO `teacher`(`name`,`mobile`,`email`,`address_id`,`gender_id`) VALUES('" + name + "','" + mobile + "','" + email + "','" + a_id + "','" + g_id + "')");
                    LoadTeachers();

                    jTextField1.setText("");
                    jTextField1.setText("");
                    jTextField3.setText("");
                    jComboBox1.setSelectedIndex(0);
                    jLabel6.setText("None");
                    jLabel7.setText("None");

                    JOptionPane.showMessageDialog(this, "Student registration successful", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
            }

        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed

        int r = jTable1.getSelectedRow();
        String t_id = jTable1.getValueAt(r, 0).toString();

        MySQL.iud("DELETE FROM `teacher` WHERE `id` = '"+t_id+"'");
        LoadTeachers();
        JOptionPane.showMessageDialog(this, "Teacher Removed", "Success", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed

        String s;

        int r = jTable1.getSelectedRow();

        if (jTable1.getValueAt(r, 6).toString().equals("Active")) {
            s = "2";
        } else {
            s = "1";
        }

        String t_id = jTable1.getValueAt(r, 0).toString();

        MySQL.iud("UPDATE `teacher` SET `status_id` = '" + s + "' WHERE `id` = '" + t_id + "'");
        LoadTeachers();
        JOptionPane.showMessageDialog(this, "Status Changed", "Success", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_jButton4ActionPerformed

