    public void LoadClasses() {
        
        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);
        
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `grade_has_class` INNER JOIN `grade` ON"
                    + " `grade_has_class`.`grade_id` = `grade`.`id` INNER JOIN `class` ON"
                    + " `class`.`id` = `grade_has_class`.`class_id` ORDER BY `grade_has_class`.`id` ASC");
            
            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("grade_has_class.id"));
                v.add(rs.getString("grade.name"));
                v.add(rs.getString("class.name"));
                dtm.addRow(v);
            }
            
        } catch (Exception e) {
        }
        
    }
    
    public void LoadCombo() {
        
        try {
            ResultSet rs = MySQL.search("SELECT * FROM `class`");
            
            Vector v = new Vector();
            v.add("Select");
            
            while (rs.next()) {
                v.add(rs.getString("name"));
                
            }
            
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);
            
        } catch (Exception e) {
            e.printStackTrace();
        }
        
    }

