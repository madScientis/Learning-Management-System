    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        String c_name = jComboBox1.getSelectedItem().toString();
        String g_id = jLabel3.getText();
        
        try {
            
            ResultSet rs = MySQL.search("SELECT * FROM `class` WHERE `name` = '" + c_name + "'");
            rs.next();
            String c_id = rs.getString("id");
            
            ResultSet rs1 = MySQL.search("SELECT * FROM `grade_has_class` WHERE `grade_id` = '" + g_id + "' AND `class_id` = '" + c_id + "'");
            if (rs1.next()) {
                JOptionPane.showMessageDialog(this, "This grade already has this class, Select it from the following table", "Warning", JOptionPane.WARNING_MESSAGE);
                
            } else {
                
                MySQL.iud("INSERT INTO `grade_has_class`(`grade_id`,`class_id`) VALUES('" + g_id + "','" + c_id + "')");
                LoadClasses();
                jComboBox1.setSelectedIndex(0);
                jLabel3.setText("None");
                JOptionPane.showMessageDialog(this, "A class has added to the grade", "Success", JOptionPane.INFORMATION_MESSAGE);
                
            }
            
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {
            
            int r = jTable1.getSelectedRow();
            String c_id = jTable1.getValueAt(r, 0).toString();
            String g_name = jTable1.getValueAt(r, 1).toString();
            String c_name = jTable1.getValueAt(r, 2).toString();
            
            if (Objects.isNull(this.ms)) {
                this.sr.jLabel12.setText(c_id);
                this.dispose();
            } else if (Objects.isNull(this.sr)) {
                
                this.ms.jLabel10.setText(c_id);
                this.ms.jLabel13.setText(g_name);
                this.ms.jLabel12.setText(c_name);
                this.dispose();
                
            }
            
        }
    }//GEN-LAST:event_jTable1MouseClicked

