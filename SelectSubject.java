    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String s_name = jTextField1.getText();

        if (s_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter subject name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet rs = MySQL.search("SELECT * FROM `subject` WHERE `name` = '" + s_name + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This subject already exists,Select it from the following table", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    MySQL.iud("INSERT INTO `subject`(`name`) VALUES('" + s_name + "')");
                    jTextField1.setText("");
                    LoadSubjects();
                    JOptionPane.showMessageDialog(this, "Subject Added", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        if (evt.getClickCount() == 2) {

            int r = jTable1.getSelectedRow();
            String s_id = jTable1.getValueAt(r, 0).toString();
            String s_name = jTable1.getValueAt(r, 1).toString();

            this.ms.jLabel6.setText(s_id);
            this.ms.jLabel7.setText(s_name);
            this.dispose();

        }
    }//GEN-LAST:event_jTable1MouseClicked

