    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String name = jTextField1.getText();
        String mobile = jTextField3.getText();
        String gender = jComboBox1.getSelectedItem().toString();
        String a_id = jLabel9.getText();
        String guardian_id = jLabel11.getText();
        String c_id = jLabel12.getText();

        if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter name", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[01245678][0-9]{7}").matcher(mobile).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid Mobile", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (gender.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select gender", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (a_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select Address", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (guardian_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select guardian", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (c_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please Select class", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet rs1 = MySQL.search("SELECT * FROM `gender` WHERE `name` = '" + gender + "'");
                rs1.next();
                String g_id = rs1.getString("id");

                ResultSet rs = MySQL.search("SELECT * FROM `student` WHERE `mobile` = '" + mobile + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "This student already exists", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    MySQL.iud("INSERT INTO `student`(`name`,`mobile`,`gender_id`,`address_id`,`guardian_id`,`grade_has_class_id`) VALUES('" + name + "','" + mobile + "','" + g_id + "','" + a_id + "','" + guardian_id + "','" + c_id + "')");
                    LoadStudents();
                    
                    jTextField1.setText("None");
                    jTextField3.setText("None");
                    jComboBox1.setSelectedIndex(0);
                    jLabel9.setText("None");
                    jLabel11.setText("None");
                    jLabel12.setText("None");
                    
                    JOptionPane.showMessageDialog(this, "Student registration successful", "Success", JOptionPane.INFORMATION_MESSAGE);

                }

            } catch (Exception e) {
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

