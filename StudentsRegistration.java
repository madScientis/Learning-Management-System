    public void LoadStudents() {

        DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
        dtm.setRowCount(0);

        try {
            ResultSet rs = MySQL.search("SELECT * FROM `student` INNER JOIN `gender` ON `student`.`gender_id` = `gender`.`id` "
                    + "INNER JOIN `address` ON `address`.`id` = `student`.`address_id` "
                    + "INNER JOIN `guardian` ON `student`.`guardian_id` = `guardian`.`id` "
                    + "INNER JOIN `status` ON `student`.`status_id` = `status`.`id` "
                    + "INNER JOIN `grade_has_class` ON `student`.`grade_has_class_id` = `grade_has_class`.`id` "
                    + "INNER JOIN `class` ON `grade_has_class`.`class_id` = `class`.`id` "
                    + "INNER JOIN `city` ON `address`.`city_id` = `city`.`id` "
                    + "INNER JOIN `grade` ON `grade_has_class`.`grade_id` = `grade`.`id` ORDER BY `student`.`id` ASC");

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("student.id"));
                v.add(rs.getString("student.name"));
                v.add(rs.getString("student.mobile"));
                v.add(rs.getString("gender.name"));
                v.add(rs.getString("address.line1") + "," + rs.getString("address.line2") + rs.getString("city.name"));
                v.add(rs.getString("grade.name") + " " + rs.getString("class.name"));
                v.add(rs.getString("guardian.name"));
                v.add(rs.getString("guardian.mobile"));
                v.add(rs.getString("status.name"));

                dtm.addRow(v);
            }

        } catch (Exception e) {
        }

    }

